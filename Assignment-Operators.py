# ------------------------------- Assignment Operators --------------------------------------------------
# Assignment operators are used to assign values to variables
# =
x=7
print(x) #7

# +=
x=7
x +=3
print(x) #10

# -=
x=7
x -= 2
print(x) #5

# *=
x= 7
x*= 3
print(x) #21

# /=
x= 7
x/= 2
print(x) #3.5

# %=
x=7
x%=4
print(x) #3

# //=
x=7
x//=2
print(x) #3

# **=
x=7
x**=5
print(x) #16807

# &=
x=7
x&=9
print(x) #1

# |=
x=7
x|=5
print(x)#7

# ^=
x=5
x^=3
print(x) #6

# >>=
x=5
x>>=3
print(x) #0

# <<=
x=5
x<<=3
print(x) #40

# :=
print(x := 9) #9

