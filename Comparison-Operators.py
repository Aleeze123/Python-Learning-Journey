# ------------------------------- Comparison Operators --------------------------------------------------
# Comparison operators are used to compare two values
x = 5
y = 3
# 	Equal
print(x == y) #False

# Not equal
print(x != y) #True

# Greater than
print(x > y) #True

# Less than
print(x < y) #False

# Greater than or equal to
print(x >= y) #True

# Less than or equal to
print(x<= y) #False